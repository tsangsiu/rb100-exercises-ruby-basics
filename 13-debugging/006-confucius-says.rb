=begin
The returned value of the method is from the last if-block. As the if-block 
returns nil, the method returns nil. And Ruby does not have an implicit way to
concert nil to a string for string concatenation, it raises TypeError.
=end

def get_quote(person)
  if person == 'Yoda'
    'Do. Or do not. There is no try.'
  elsif person == 'Confucius'
    'I hear and I forget. I see and I remember. I do and I understand.'
  elsif person == 'Einstein'
    'Do not worry about your difficulties in Mathematics. I can assure you mine are still greater.'
  end
end

puts 'Confucius says:'
puts '"' + get_quote('Confucius') + '"'